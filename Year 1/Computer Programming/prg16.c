// Write a program to pprint a table of equation (x/2+y)+2,
// where x is in range of (5.5,12.5,.5) and y is in the range of (1,6)

#include <stdio.h>

void main() {
	float i, y, x;
	printf(" Y  \t X  \t I\n");
	for (y = 1;y <= 6;y++) {
		for (x = 5.5;x <= 12.5;x += .5) {
			i = 2 + (y + .5 * x);
			printf("%.2f\t%.2f\t%.2f\n", y, x, i);
		}
	}
}

